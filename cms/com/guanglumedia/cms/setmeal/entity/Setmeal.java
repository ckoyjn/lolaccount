package com.guanglumedia.cms.setmeal.entity;

import java.util.Date;

/**
 * 套餐
 */
public class Setmeal {
	private int id;
	private String name;
	private String price;
	private String discountprice;
	private String description;
	private String formnum;
	private Date putawaystime;
	private Date putawayetime;
	private int status;
	private int sort;
	private String identify;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getDiscountprice() {
		return discountprice;
	}
	public void setDiscountprice(String discountprice) {
		this.discountprice = discountprice;
	}
	public String getFormnum() {
		return formnum;
	}
	public void setFormnum(String formnum) {
		this.formnum = formnum;
	}
	public Date getPutawaystime() {
		return putawaystime;
	}
	public void setPutawaystime(Date putawaystime) {
		this.putawaystime = putawaystime;
	}
	public Date getPutawayetime() {
		return putawayetime;
	}
	public void setPutawayetime(Date putawayetime) {
		this.putawayetime = putawayetime;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getIdentify() {
		return identify;
	}
	public void setIdentify(String identify) {
		this.identify = identify;
	}

}
